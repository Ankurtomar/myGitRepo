package print;

public class Print {
	public Print(){
		
	}
	public Print(String s){
		System.out.println(s);
	}
	public Print(Object o){
		System.out.println(o);
	}
	public void Printa(String s){
		System.out.print(s);
	}
	public void Printa(Object o){
		System.out.print(o);
	}
}
